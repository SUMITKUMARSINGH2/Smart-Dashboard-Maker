from flask import Blueprint, request, jsonify, send_file, make_response
import pandas as pd
import numpy as np
import io, json, zipfile, traceback
from api.store import get_store

export_bp = Blueprint("export", __name__, url_prefix="/api/export")

def _df(): return get_store().get("df")

@export_bp.route("/csv")
def export_csv():
    df = _df()
    if df is None: return jsonify(error="No dataset"), 400
    buf = io.StringIO()
    df.to_csv(buf, index=False)
    resp = make_response(buf.getvalue())
    resp.headers["Content-Type"] = "text/csv"
    fn = get_store().get("filename","dataset").rsplit(".",1)[0]
    resp.headers["Content-Disposition"] = f"attachment; filename={fn}_cleaned.csv"
    return resp

@export_bp.route("/excel")
def export_excel():
    df = _df()
    if df is None: return jsonify(error="No dataset"), 400
    buf = io.BytesIO()
    with pd.ExcelWriter(buf, engine="openpyxl") as w:
        df.to_excel(w, index=False, sheet_name="Data")
        # stats sheet
        num = df.select_dtypes(include="number")
        if len(num.columns):
            num.describe().to_excel(w, sheet_name="Statistics")
    buf.seek(0)
    fn = get_store().get("filename","dataset").rsplit(".",1)[0]
    return send_file(buf, download_name=f"{fn}_export.xlsx",
                     as_attachment=True, mimetype="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet")

