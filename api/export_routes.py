@export_bp.route("/zip")
def export_zip():
    df = _df()
    if df is None: return jsonify(error="No dataset"), 400
    try:
        store = get_store()
        fn = store.get("filename","dataset").rsplit(".",1)[0]
        buf = io.BytesIO()
        with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
            # CSV
            csv_buf = io.StringIO(); df.to_csv(csv_buf, index=False)
            zf.writestr(f"{fn}.csv", csv_buf.getvalue())
            # JSON
            j = df.where(pd.notnull(df), None).to_json(orient="records", indent=2)
            zf.writestr(f"{fn}.json", j)
            # Stats txt
            num = df.select_dtypes(include="number")
            if len(num.columns):
                zf.writestr("statistics.txt", num.describe().round(4).to_string())
        buf.seek(0)
        return send_file(buf, download_name=f"{fn}_bundle.zip",
                         as_attachment=True, mimetype="application/zip")
    except Exception as e:
        return jsonify(error=str(e)), 500

@export_bp.route("/powerbi-mquery")
def export_powerbi():
    store = get_store()
    df = store.get("df")
    if df is None: return jsonify(error="No dataset"), 400
    fn = store.get("filename","dataset")
    cols = list(df.columns)
    col_types = []
    for c in cols:
        dt = str(df[c].dtype)
        if "int" in dt: pbi_type = "Int64.Type"
        elif "float" in dt: pbi_type = "type number"
        elif "datetime" in dt: pbi_type = "type datetime"
        else: pbi_type = "type text"
        col_types.append(f'{{"{c}", {pbi_type}}}')
    col_types_str = ",\n            ".join(col_types)
    m_query = f"""let
    Source = Csv.Document(File.Contents("{fn}"), [Delimiter=",", Columns={len(cols)}, Encoding=1252, QuoteStyle=QuoteStyle.None]),
    PromoteHeaders = Table.PromoteHeaders(Source, [PromoteAllScalars=true]),
    ChangedTypes = Table.TransformColumnTypes(PromoteHeaders, {{
        {col_types_str}
    }})
in
    ChangedTypes"""
    dax = _generate_dax(df)
    resp = make_response(json.dumps({"m_query": m_query, "dax_measures": dax}, indent=2))
    resp.headers["Content-Type"] = "application/json"
    resp.headers["Content-Disposition"] = "attachment; filename=powerbi_config.json"
    return resp

def _generate_dax(df):
    measures = []
    num_cols = df.select_dtypes(include="number").columns
    for c in num_cols[:5]:
        measures.append(f"Total {c} = SUM('{c}'[{c}])")
        measures.append(f"Avg {c} = AVERAGE('{c}'[{c}])")
    return measures

