def _m_query(df: pd.DataFrame, filename: str) -> str:
    col_types = []
    for col in df.columns:
        dtype = df[col].dtype
        if pd.api.types.is_integer_dtype(dtype): pbi_type = "Int64.Type"
        elif pd.api.types.is_float_dtype(dtype): pbi_type = "type number"
        elif pd.api.types.is_datetime64_any_dtype(dtype): pbi_type = "type datetime"
        elif pd.api.types.is_bool_dtype(dtype): pbi_type = "type logical"
        else: pbi_type = "type text"
        col_types.append(f'        {{"{col}", {pbi_type}}}')
    types_str = ",\n".join(col_types)

    return textwrap.dedent(f"""
    // Power Query M Script — generated by DataViz Pro
    let
        Source = Excel.Workbook(
            File.Contents("C:\\\\Users\\\\YOU\\\\Downloads\\\\{filename}"),
            null, true
        ),
        CleanedData_Sheet = Source{{[Item="Cleaned Data", Kind="Sheet"]}}[Data],
        #"Promoted Headers" = Table.PromoteHeaders(CleanedData_Sheet, [PromoteAllScalars=true]),
        #"Changed Type" = Table.TransformColumnTypes(
            #"Promoted Headers",
            {{
    {types_str}
            }}
        )
    in
        #"Changed Type"
    """).strip()


def _dax_measures(df: pd.DataFrame) -> str:
    num_cols = df.select_dtypes(include="number").columns.tolist()
    if not num_cols:
        return "// No numeric columns found."
    lines = ["// ── Suggested DAX Measures — paste into Power BI Desktop ──\n"]
    for col in num_cols[:8]:
        lines += [
            f"// {col}",
            f"[Total {col}] = SUM('Table'[{col}])",
            f"[Avg {col}] = AVERAGE('Table'[{col}])",
            f"[Max {col}] = MAX('Table'[{col}])",
            f"[Min {col}] = MIN('Table'[{col}])",
            "",
        ]
    return "\n".join(lines)


