    for (val, lbl), color in zip(kpis, kpi_colors):
        parts.append(f'<div class="kpi"><div class="kpi-val" style="color:{color}">{val}</div><div class="kpi-lbl">{lbl}</div></div>')
    parts.append("</div>")

